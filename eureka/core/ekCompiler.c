static int ekCompileOptimizeChild(struct ekContext *Y, ekCompiler *compiler, ekSyntax *syntax);

static void ekCompileOptimizeArray(struct ekContext *Y, ekCompiler *compiler, ekSyntax **a)
{
    int i;
    for(i = 0; i < ekArraySize(Y, &a); i++)
    {
        ekCompileOptimizeChild(Y, compiler, (ekSyntax *)a[i]);
    }
}

static int ekCompileOptimizeChild(struct ekContext *Y, ekCompiler *compiler, ekSyntax *syntax)
{
    if(syntax->v.a) { ekCompileOptimizeArray(Y, compiler, syntax->v.a); }
    if(syntax->v.p) { ekCompileOptimizeChild(Y, compiler, syntax->v.p); }
    if(syntax->l.a) { ekCompileOptimizeArray(Y, compiler, syntax->l.a); }
    if(syntax->l.p) { ekCompileOptimizeChild(Y, compiler, syntax->l.p); }
    if(syntax->r.a) { ekCompileOptimizeArray(Y, compiler, syntax->r.a); }
    if(syntax->r.p) { ekCompileOptimizeChild(Y, compiler, syntax->r.p); }

    switch(syntax->type)
    {
        case YST_ADD:
        case YST_SUB:
        case YST_MUL:
        case YST_DIV:
        {
            // Integer arithmetic optimization
            if((syntax->l.p->type == YST_KINT)
               && (syntax->r.p->type == YST_KINT))
            {
                int val;
                switch(syntax->type)
                {
                    case YST_ADD:
                        val = syntax->l.p->v.i + syntax->r.p->v.i;
                        break;
                    case YST_SUB:
                        val = syntax->l.p->v.i - syntax->r.p->v.i;
                        break;
                    case YST_MUL:
                        val = syntax->l.p->v.i * syntax->r.p->v.i;
                        break;
                    case YST_DIV:
                        if(!syntax->r.p->v.i) { return 0; }
                        val = syntax->l.p->v.i / syntax->r.p->v.i;
                        break;
                };
                ekSyntaxDestroy(Y, syntax->l.p);
                syntax->l.p = NULL;
                ekSyntaxDestroy(Y, syntax->r.p);
                syntax->r.p = NULL;
                syntax->type = YST_KINT;
                syntax->v.i = val;
            }
        }
        break;
    };

    return 0;
}

void ekCompileOptimize(struct ekContext *Y, ekCompiler *compiler)
{
    ekCompileOptimizeChild(Y, compiler, compiler->root);
}

