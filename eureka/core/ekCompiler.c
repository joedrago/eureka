ekCompiler *ekCompilerCreate(struct ekContext *Y)
{
    ekCompiler *compiler = (ekCompiler *)ekAlloc(sizeof(ekCompiler));
    compiler->Y = Y;
    return compiler;
}

void ekCompilerDestroy(ekCompiler *compiler)
{
    struct ekContext *Y = compiler->Y;
    if(compiler->chunk)
    {
        ekChunkDestroy(Y, compiler->chunk);
    }
    if(compiler->root)
    {
        ekSyntaxDestroy(Y, compiler->root);
    }
    ekArrayDestroy(Y, &compiler->errors, (ekDestroyCB)ekDestroyCBFree);
    ekFree(compiler);
}

yBool ekCompile(ekCompiler *compiler, const char *text, yU32 compileOpts)
{
    struct ekContext *Y = compiler->Y;
    yBool success = yFalse;
    ekToken emptyToken = {0};
    void *parser;

#ifdef EUREKA_TRACE_PARSER
    ekParseTrace(stderr, "--- ");
#endif

    ekTraceMem(("\n                                     "
                 "--- start chunk compile ---\n"));

    parser = ekParseAlloc(Y);
    compiler->root = NULL;

    ekLex(parser, text, ekParse, compiler);
    ekParse(parser, 0, emptyToken, compiler);

    if(compiler->root)
    {
        if(!ekArraySize(Y, &compiler->errors))
        {
            if(compileOpts & YCO_OPTIMIZE)
            {
                ekCompileOptimize(Y, compiler);
            }

            ekAssemble(Y, compiler);
            success = yTrue;
        }
        if(!(compileOpts & YCO_KEEP_SYNTAX_TREE))
        {
            ekSyntaxDestroy(Y, compiler->root);
            compiler->root = NULL;
        };
    }
    else
    {
        ekContextSetError(Y, YVE_COMPILE, "unknown badness - grammar is probably incomplete");
    }

    ekParseFree(Y, parser);

    ekTraceMem(("                                     "
                 "---  end  chunk compile ---\n\n"));

    return success;
}

void ekCompileError(ekCompiler *compiler, const char *error)
{
    struct ekContext *Y = compiler->Y;
    ekArrayPush(Y, &compiler->errors, ekStrdup(Y, error));
}

static void appendInt(char *text, int i)
{
    char temp[32];
    sprintf(temp, "%d", i);
    strcat(text, temp);
}

void ekCompileSyntaxError(ekCompiler *compiler, struct ekToken *token)
{
    ekContext *Y = compiler->Y;
    char error[64];
    strcpy(error, "Line [");
    appendInt(error, token->line);
    strcat(error, "]: ");
    strcat(error, "syntax error near '");
    if(token && token->text)
    {
        char *newlinePos;
        char temp[32];
        int len = strlen(token->text);
        if(len > 31) { len = 31; }
        memcpy(temp, token->text, len);
        temp[len] = 0;
        newlinePos = strchr(temp, '\r');
        if(!newlinePos)
        {
            newlinePos = strchr(temp, '\n');
        }
        if(newlinePos)
        {
            *newlinePos = 0;
        }
        strcat(error, temp);
    }
    else
    {
        strcat(error, "<unknown>");
    }
    strcat(error, "'");

    ekCompileError(compiler, error);
}

